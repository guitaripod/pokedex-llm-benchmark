#!/usr/bin/env node
import { readFileSync, writeFileSync, existsSync } from "node:fs";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const ROOT = join(dirname(fileURLToPath(import.meta.url)), "..");
const manifest = JSON.parse(readFileSync(join(ROOT, "submissions.json"), "utf8"));
const checklist = JSON.parse(
  readFileSync(join(ROOT, "docs", "feature-checklist.json"), "utf8"),
);

const MARK = { present: "●", partial: "◐", absent: "○" };
const TOTAL = checklist.categories.reduce((n, c) => n + c.features.length, 0);

const statusOf = (sub, fid) =>
  (sub.features || []).find((f) => f.id === fid)?.status || "absent";
const evidenceOf = (sub, fid) =>
  (sub.features || []).find((f) => f.id === fid)?.evidence || "";

function entry(sub) {
  const title =
    sub.effort && sub.effort !== "default"
      ? `${sub.model} — ${sub.effort}`
      : sub.model;
  const full = (sub.features || []).filter((f) => f.status === "present").length;
  const partial = (sub.features || []).filter((f) => f.status === "partial").length;

  const lines = [];
  lines.push(`<!-- GENERATED by scripts/gen-entries.mjs from submissions.json — do not edit by hand. -->\n`);
  lines.push(`# ${title}\n`);
  if (sub.assessment?.summary) lines.push(`> ${sub.assessment.summary}\n`);

  lines.push(`| | |`);
  lines.push(`|---|---|`);
  lines.push(`| **Model** | ${sub.model} (${sub.provider}${sub.modelVersion ? `, ${sub.modelVersion}` : ""}) |`);
  lines.push(`| **Effort** | ${sub.effort} |`);
  lines.push(`| **Built** | ${sub.date} |`);
  lines.push(`| **Live** | ${sub.liveUrl ? `<${sub.liveUrl}>` : "—"} |`);
  lines.push(`| **Source repo** | <${sub.sourceRepo}> |`);
  lines.push(`| **Platform** | ${sub.platform} |`);
  if (sub.stack)
    lines.push(`| **Stack** | ${sub.stack.framework} · ${sub.stack.language} · ${sub.stack.bundler} · ${sub.stack.styling} |`);
  if (sub.dataStrategy) lines.push(`| **Data strategy** | ${sub.dataStrategy} |`);
  if (sub.metrics)
    lines.push(`| **Source** | ${sub.metrics.sourceLoc.toLocaleString("en-US")} LOC · ${sub.metrics.sourceFiles} files · ${sub.metrics.dependencies}+${sub.metrics.devDependencies} deps |`);
  lines.push(`| **Feature coverage** | ${full} / ${TOTAL}${partial ? ` (+${partial} partial)` : ""} |`);
  lines.push("");

  if (sub.scores) {
    lines.push(`## Scorecard\n`);
    lines.push(`| Completeness | Code quality | Architecture | UX polish |`);
    lines.push(`|:---:|:---:|:---:|:---:|`);
    lines.push(`| ${sub.scores.completeness} | ${sub.scores.codeQuality} | ${sub.scores.architecture} | ${sub.scores.uxPolish} |`);
    lines.push(`\n_Scored 0–10 from source; see [RUBRIC.md](../../RUBRIC.md)._\n`);
  }

  if (sub.assessment) {
    const a = sub.assessment;
    if (a.strengths?.length) {
      lines.push(`## Strengths\n`);
      for (const s of a.strengths) lines.push(`- ${s}`);
      lines.push("");
    }
    if (a.weaknesses?.length) {
      lines.push(`## Weaknesses\n`);
      for (const s of a.weaknesses) lines.push(`- ${s}`);
      lines.push("");
    }
    if (a.standout) lines.push(`## Standout\n\n${a.standout}\n`);
  }

  lines.push(`## Feature coverage detail\n`);
  lines.push(`Legend: ${MARK.present} present · ${MARK.partial} partial · ${MARK.absent} absent\n`);
  for (const cat of checklist.categories) {
    lines.push(`**${cat.label}**\n`);
    lines.push(`| Feature | | Evidence |`);
    lines.push(`|---|:---:|---|`);
    for (const f of cat.features) {
      const st = statusOf(sub, f.id);
      const ev = st === "absent" ? "" : `\`${evidenceOf(sub, f.id)}\``;
      lines.push(`| ${f.label} | ${MARK[st]} | ${ev} |`);
    }
    lines.push("");
  }

  lines.push(`## Vendored source\n`);
  lines.push(
    `The source in this directory is the code the model wrote, copied verbatim from [its repo](${sub.sourceRepo}) minus \`node_modules\`, build output, and generated data.`,
  );
  if (sub.dataNote) lines.push(`\n> ${sub.dataNote}`);
  lines.push(`\nCanonical history and issues live in the [original repo](${sub.sourceRepo}). This is a scored snapshot for the benchmark.\n`);

  return lines.join("\n");
}

let n = 0;
for (const sub of manifest.submissions) {
  const dir = join(ROOT, "submissions", sub.id);
  if (!existsSync(dir)) continue;
  writeFileSync(join(dir, "ENTRY.md"), entry(sub));
  n += 1;
}
console.log(`Wrote ${n} ENTRY.md scorecards.`);
