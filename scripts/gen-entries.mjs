#!/usr/bin/env node
import { readFileSync, writeFileSync, existsSync } from "node:fs";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";
import {
  AXIS_KEYS, COVERAGE_WEIGHT, AXES_WEIGHT,
  totalFeatures, maxDepth, gradeOf, featureDepth, bench as benchRaw,
} from "./lib/score.mjs";

const ROOT = join(dirname(fileURLToPath(import.meta.url)), "..");
const manifest = JSON.parse(readFileSync(join(ROOT, "submissions.json"), "utf8"));
const checklist = JSON.parse(readFileSync(join(ROOT, "docs", "feature-checklist.json"), "utf8"));

const GRADE_MARK = { 3: "●", 2: "◕", 1: "◔", 0: "○" };
const AXIS_LABEL = { codeQuality: "Code quality", architecture: "Architecture", uxDesign: "UX & design", robustness: "Robustness" };
const TOTAL = totalFeatures(checklist);
const MAX_QUALITY = maxDepth(checklist);
const feat = (sub, fid) => (sub.features || []).find((f) => f.id === fid);
const featureQuality = (sub) => featureDepth(sub, checklist);
const bench = (sub) => benchRaw(sub, checklist);

function entry(sub) {
  const title = sub.effort && sub.effort !== "default" ? `${sub.model} — ${sub.effort}` : sub.model;
  const solid = checklist.categories.reduce(
    (n, c) => n + c.features.filter((f) => gradeOf(sub, f.id) >= 2).length,
    0,
  );
  const scored = sub.features && sub.features.length && sub.scores;

  const L = [];
  L.push(`<!-- GENERATED by scripts/gen-entries.mjs from submissions.json — do not edit by hand. -->\n`);
  L.push(`# ${title}\n`);
  if (sub.assessment?.summary) L.push(`> ${sub.assessment.summary}\n`);

  L.push(`| | |`, `|---|---|`);
  if (scored) L.push(`| **Benchmark score** | **${bench(sub).toFixed(1)} / 100** |`);
  L.push(`| **Model** | ${sub.model} (${sub.provider}${sub.modelVersion ? `, ${sub.modelVersion}` : ""}) |`);
  L.push(`| **Effort** | ${sub.effort} |`);
  L.push(`| **Built** | ${sub.date} |`);
  L.push(`| **Live** | ${sub.liveUrl ? `<${sub.liveUrl}>` : "—"} |`);
  L.push(`| **Source repo** | <${sub.sourceRepo}> |`);
  L.push(`| **Platform** | ${sub.platform} |`);
  if (sub.provenance) {
    const p = sub.provenance;
    const bits = [
      p.oneShot && "one-shot",
      p.autonomous && "autonomous",
      p.selfProvisioned ? "self-provisioned" : "deploy-only (no self-created repo)",
      `verified: ${p.verified}`,
    ].filter(Boolean);
    L.push(`| **Provenance** | ${bits.join(" · ")} |`);
  }
  if (sub.grading) L.push(`| **Graded** | ${sub.grading.gradedBy} · ${sub.grading.gradedOn} · rubric v${sub.grading.rubricVersion} |`);
  if (sub.stack) L.push(`| **Stack** | ${sub.stack.framework} · ${sub.stack.language} · ${sub.stack.bundler} · ${sub.stack.styling} |`);
  if (sub.dataStrategy) L.push(`| **Data strategy** | ${sub.dataStrategy} |`);
  if (sub.runtime) {
    const r = sub.runtime;
    const tick = (b) => (b ? "✓" : "✗");
    L.push(`| **Runtime check** | **${r.verdict}** — loads ${tick(r.loadOk)} · content ${tick(r.contentOk)} · JS exceptions ${r.pageErrors} · console errors ${r.consoleErrors} · detail route ${tick(r.detailOk)} (headless, ${r.checkedAt}) |`);
  }
  if (sub.metrics) L.push(`| **Source** | ${sub.metrics.sourceLoc.toLocaleString("en-US")} LOC · ${sub.metrics.sourceFiles} files · ${sub.metrics.dependencies}+${sub.metrics.devDependencies} deps |`);
  if (scored) L.push(`| **Feature depth** | ${featureQuality(sub)} / ${MAX_QUALITY} (${solid}/${TOTAL} features solid or better) |`);
  L.push("");
  if (sub.notes) L.push(`> ⚙️ **Run note:** ${sub.notes}\n`);

  if (sub.scores) {
    L.push(`## Scorecard\n`);
    L.push(`| ${AXIS_KEYS.map((k) => AXIS_LABEL[k]).join(" | ")} |`);
    L.push(`|${":---:|".repeat(AXIS_KEYS.length)}`);
    L.push(`| ${AXIS_KEYS.map((k) => sub.scores[k]).join(" | ")} |`);
    L.push(`\n_Four orthogonal axes, 0–10 from source. Benchmark score = ${COVERAGE_WEIGHT * 100}% feature depth + ${AXES_WEIGHT * 100}% axis average. See [RUBRIC.md](../../RUBRIC.md)._\n`);
  }

  if (sub.assessment) {
    const a = sub.assessment;
    if (a.strengths?.length) L.push(`## Strengths\n`, ...a.strengths.map((s) => `- ${s}`), "");
    if (a.weaknesses?.length) L.push(`## Weaknesses\n`, ...a.weaknesses.map((s) => `- ${s}`), "");
    if (a.standout) L.push(`## Standout\n\n${a.standout}\n`);
  }

  L.push(`## Feature depth detail\n`);
  L.push(`Legend: ${GRADE_MARK[3]} exceptional (3) · ${GRADE_MARK[2]} solid (2) · ${GRADE_MARK[1]} shallow/broken (1) · ${GRADE_MARK[0]} absent (0)\n`);
  for (const cat of checklist.categories) {
    L.push(`**${cat.label}**\n`);
    L.push(`| Feature | Grade | Notes |`, `|---|:---:|---|`);
    for (const f of cat.features) {
      const g = gradeOf(sub, f.id);
      const ev = g === 0 ? "" : (feat(sub, f.id)?.evidence ? `\`${feat(sub, f.id).evidence}\`` : "");
      L.push(`| ${f.label} | ${GRADE_MARK[g]} ${g} | ${ev} |`);
    }
    L.push("");
  }

  L.push(`## Vendored source\n`);
  L.push(`The source in this directory is the code the model wrote, copied verbatim from [its repo](${sub.sourceRepo}) minus \`node_modules\`, build output, and generated data.`);
  if (sub.dataNote) L.push(`\n> ${sub.dataNote}`);
  L.push(`\nCanonical history and issues live in the [original repo](${sub.sourceRepo}). This is a scored snapshot for the benchmark.\n`);

  return L.join("\n");
}

let n = 0;
for (const sub of manifest.submissions) {
  const dir = join(ROOT, "submissions", sub.id);
  if (!existsSync(dir)) continue;
  writeFileSync(join(dir, "ENTRY.md"), entry(sub));
  n += 1;
}
console.log(`Wrote ${n} ENTRY.md scorecards.`);
